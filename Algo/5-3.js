// GIVEN AN ARRAY, RETURN ARRAY WITHOUT DUPLICATES
// [1,2,1,3,4,2] => [1,2,3,4]

// CHALLENGE RETURN THE SAME ARRAY

function removeDupe(arr) {
arr = arr 
temparr = []
for(var i = 0; i < arr.length; i++ );
    temparr[i] = arr[i]
}

console.log(removeDupe([1,2,1,3,4,2]))// output == [1,2,3,4]
